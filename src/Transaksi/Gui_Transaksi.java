    public void KodeOtomatisRental() {
        try {
            GregorianCalendar gc = new GregorianCalendar();
            SimpleDateFormat sf = new SimpleDateFormat("dd/MM/yy");
            String kp = FieldIDPelanggan.getText();
            String kodemasuk = "YULIUS/RENTALBARANG/KELUAR/" + kp + "/" + sf.format(gc.getTime()) + "/";
            Date tgl = new Date();
            Date ask = new java.sql.Date(tgl.getTime());
            SimpleDateFormat fformat = new SimpleDateFormat("dd-MMM-yy");
            String awal = fformat.format(ask);
            String bantu;
            int kode;
            kode = Kontrol_Transaksi.getKoneksi().kodeOtomatis(new java.sql.Date(tgl.getTime()), kp) + 1;
//            System.out.println(sf.format(ask));
//            System.out.println(sf.format(gc.getTime()));
//            System.out.println(awal);
            System.out.println(new java.sql.Date(tgl.getTime()));
            if (kode == 1) {
                bantu = "001";
                fieldIDTransaksiRental.setText(kodemasuk + bantu);
            } else if (kode < 10) {
                bantu = "00";
                fieldIDTransaksiRental.setText(kodemasuk + bantu + Integer.toString(kode));
            } else if (10 <= kode && kode < 100) {
                bantu = "0";
                fieldIDTransaksiRental.setText(kodemasuk + bantu + Integer.toString(kode));
            } else {
                fieldIDTransaksiRental.setText(kodemasuk + Integer.toString(kode));
            }
        } catch (SQLException ex) {
            ex.getMessage();
        }

    }

