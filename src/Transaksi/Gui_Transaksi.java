    private void buttonTambahRentalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahRentalActionPerformed
        try {
            // TODO add your handling code here:
            String namatombol = buttonTambahRental.getText();
            String idTransaksiRental = fieldIDTransaksiRental.getText();
            int jumlahBarang = Integer.parseInt(fieldJumlahRental.getText());
            int lama = Integer.parseInt(fieldLamaRental.getText());
            String kode = fieldKodeBarang.getText();

            DetailTransaksi r = new DetailTransaksi();
            r.setId(String.valueOf(Kontrol_DetailTransaksi.getkoneksi().kodeOtomatis()));

            Barang b = new Barang();
            b.setKodeBarang(kode);
            r.setKodebarang(b);

            Transaksi t = new Transaksi();
            t.setIdTransaksi(idTransaksiRental);
            r.setIdTranksai(t);

            r.setJumlahBarang(jumlahBarang);
            r.setLamaPinjam(lama);
            Barang brg = KontrolBarang.getKoneksi().LihatSatuBarang(kode);
            long harga = (long) (lama * brg.getHargasewa());
            r.setTotalHarga(jumlahBarang * harga);

            if (fieldIDTransaksiRental.getText().equals("") || fieldKodeBarang.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Silahkan masukkan data dengan benar");
            } else {
                Kontrol_DetailTransaksi kon = Kontrol_DetailTransaksi.getkoneksi().getkoneksi();

                if (namatombol.equals("UPDATE")) {
                    r.setId(label_Bantu_idDetailTransaksi.getText());
                    kon.UpdateDetailTransaksi(r);

                    fieldNamaBarang.setText("");
                    fieldJumlahRental.setText("0");
                    fieldLamaRental.setText("0");
                    fieldKodeBarang.setText("");
                    buttonTambahRental.setText("TAMBAH");

                    fieldLamaRental.setEditable(false);
                    fieldJumlahRental.setEditable(false);

                } else {
                    kon.InsertDetailTransaksi(r);

                    fieldNamaBarang.setText("");
                    fieldJumlahRental.setText("0");
                    fieldLamaRental.setText("0");
                    fieldKodeBarang.setText("");

                    fieldLamaRental.setEditable(false);
                    fieldJumlahRental.setEditable(false);
                }

                tampilDetailTransaksi(fieldIDTransaksiRental.getText());

            }
        } catch (SQLException ex) {
            ex.getMessage();
        }
    }//GEN-LAST:event_buttonTambahRentalActionPerformed

